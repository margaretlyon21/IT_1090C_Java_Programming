package Lab4.Lab_04_Java_Variables;

public class Lab_04_04_EvenOdd {
    public static void main(String[] args) {
        int numToExamine = 10;
        if (numToExamine % 2 == 0) {
            System.out.println("The number " + numToExamine + " is even");
        }
        else {
            System.out.println("The number is odd");
        }

    }
}
