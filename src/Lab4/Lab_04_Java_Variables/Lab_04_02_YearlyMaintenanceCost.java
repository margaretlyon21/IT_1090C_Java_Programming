public class Lab_04_02_YearlyMaintenanceCost {
    public static void main(String[] args) {
        double winterCost = 120;
        double springCost = 400.50;
        double summerCost = 327;
        double fallCost = 667.7;
        double sum = winterCost + springCost + summerCost + fallCost;
        System.out.println("The yearly maintenence cost is " + sum + ". The winter cost was " + winterCost + ", the spring cost was " + springCost + ", the summer cost was " + summerCost + ", and the fall cost was " + fallCost);

    }
}
